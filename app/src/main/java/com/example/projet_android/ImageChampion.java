package com.example.projet_android;

class ImageChampion {

    private String full;
    private String sprite;
    private String group;
    private Integer x;
    private Integer y;
    private Integer w;
    private Integer h;

    public String getFull() {
        return full;
    }

    public String getSprite() {
        return sprite;
    }

    public String getGroup() {
        return group;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

    public Integer getW() {
        return w;
    }

    public Integer getH() {
        return h;
    }
}
